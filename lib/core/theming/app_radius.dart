/// Standard corner-radius scale. Use with the responsive `.r` extension, e.g.
/// `BorderRadius.circular(AppRadius.md.r)`.
class AppRadius {
  static const double sm = 8; // small pills, step buttons, progress tracks
  static const double md = 12; // buttons, list rows, chips
  static const double lg = 16; // cards, bottom sheets, dialogs
  static const double xl = 24; // hero / header card
  static const double pill = 100; // fully rounded
}
